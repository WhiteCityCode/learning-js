/**
 * You are part of a team that builds various structures using only strings.
 * Todays client has ordered a function that builds him a staircase with the
 * requested number of steps. A staircase with n = 4 steps looks like so:
 *    #
 *   ##
 *  ###
 * ####
 *
 * It's base and height are both equal to n;
 * Your function should return a string that, when printed to the console,
 * returns a staircase of size n;
 *
 * Example:
 * stringBuilders(4) ->
 *    #
 *   ##
 *  ###
 * ####
 */

export const stringBuilders = (n: number): string => {
  return n.toString();
};
